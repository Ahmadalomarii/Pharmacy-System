    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         
        int Number=Integer.parseInt(jTextField3.getText());
         if(Medicines.size()==0){JOptionPane.showMessageDialog(this,"You Can't Delet From Empty List"+"ERORR"+JOptionPane.ERROR_MESSAGE);}
       for(int i=0;i<Medicines.size();i++){
       if(Medicines.get(i).getNumberOfMedicine()==Number){
           Medicines.remove(i);
           DefaultTableModel tableModel = (DefaultTableModel)jTable1. getModel();
          tableModel.getDataVector().remove(i);
          tableModel.fireTableDataChanged();break;
       }
       }
   
    }//GEN-LAST:event_jButton4ActionPerformed

